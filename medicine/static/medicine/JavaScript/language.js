let currentLanguage = "en";
function changeLanguage(selectedLanguage) {
    var contactPage, loginPage, signupPage, allconfirmPage, paymentPage, homePage;

    if (selectedLanguage === 'en') {
        contactPage = 'contact.html';
        loginPage = 'login.html';
        signupPage = 'signup.html';
        allconfirmPage = 'allconfirm.html';
        paymentPage = 'payment.html';
        homePage = 'index.html';
    } else if (selectedLanguage === 'tr') {
        contactPage = 'contact_tr.html';
        loginPage = 'login_tr.html';
        signupPage = 'signup_tr.html';
        allconfirmPage = 'allconfirm_tr.html';
        paymentPage = 'payment_tr.html';
        homePage = 'index_tr.html';
    }

    document.querySelector('.contact-button').setAttribute('href', contactPage);
    document.querySelector('.login-button').setAttribute('href', loginPage);
    document.querySelector('.signup-button').setAttribute('href', signupPage);
    document.querySelector('.allconfirm-button').setAttribute('href', allconfirmPage);
    document.querySelector('.payment-button').setAttribute('href', paymentPage);
    document.querySelector('.home-button').setAttribute('href', homePage);
}